    model = InceptionV3([block_idx])
    if cuda:
        model.cuda()
    act1, m1, s1 = _compute_statistics_of_path(path_real, model, batch_size,
                                              dims, cuda)
    act2, m2, s2 = _compute_statistics_of_path(path_fake, model, batch_size,
                                              dims, cuda)
    fid_value = calculate_frechet_distance(m1, s1, m2, s2)
